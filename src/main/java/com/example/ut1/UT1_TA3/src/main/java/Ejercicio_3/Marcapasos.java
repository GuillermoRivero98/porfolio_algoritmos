package com.example.ut1.UT1_TA3.src.main.java.Ejercicio_3;

public class Marcapasos {

    private int presionSanguinea; 
    private int frecuenciaCardiaca; 
    private int nivelAzucar; 
    private long maxFuerzaExpuesta; 
    private float minTiempoEntreLatidos; 
    private float bateriaRestante; 
    private String codigoFabricante; 

    public Marcapasos() {
        
    }

    public int getPresionSanguinea() {
        return presionSanguinea;
    }

    public void setPresionSanguinea(int presionSanguinea) {
        this.presionSanguinea = presionSanguinea;
    }

    public int getFrecuenciaCardiaca() {
        return frecuenciaCardiaca;
    }

    public void setFrecuenciaCardiaca(int frecuenciaCardiaca) {
        this.frecuenciaCardiaca = frecuenciaCardiaca;
    }

    public int getNivelAzucar() {
        return nivelAzucar;
    }

    public void setNivelAzucar(int nivelAzucar) {
        this.nivelAzucar = nivelAzucar;
    }

    public long getMaxFuerzaExpuesta() {
        return maxFuerzaExpuesta;
    }

    public void setMaxFuerzaExpuesta(long maxFuerzaExpuesta) {
        this.maxFuerzaExpuesta = maxFuerzaExpuesta;
    }

    public float getMinTiempoEntreLatidos() {
        return minTiempoEntreLatidos;
    }

    public void setMinTiempoEntreLatidos(float minTiempoEntreLatidos) {
        this.minTiempoEntreLatidos = minTiempoEntreLatidos;
    }

    public float getBateriaRestante() {
        return bateriaRestante;
    }

    public void setBateriaRestante(float bateriaRestante) {
        this.bateriaRestante = bateriaRestante;
    }

    public String getCodigoFabricante() {
        return codigoFabricante;
    }

    public void setCodigoFabricante(String codigoFabricante) {
        this.codigoFabricante = codigoFabricante;
    }
}
